def soma(num1, num2):
    resultado = num1 + num2
    return resultado

def subtração(num1, num2):
    resultado = num1 - num2
    return resultado

def multiplicação(num1, num2):
    resultado = num1 * num2
    return resultado
    
def divisão(num1, num2):
   resultado = num1 / num2
   return resultado
